function [ out ] = assembleEms( in )
out = [in.nh_ch4,   in.sh_ch4,   in.nh_13ch4, in.sh_13ch4, ...
       in.nh_14ch4, in.sh_14ch4, in.nh_ch3d,  in.sh_ch3d,  ...
       in.nh_oh,    in.sh_oh,    in.nh_co,    in.sh_co,    ...
       in.tau_TS,   in.kX_NH,    in.kX_SH,    in.cl_conc];
end


