%%% =  ( 1): Converts a fractional year to a julian date.
%%% =----------------------------------------------------------------------
%%% = INPUTS
%%% =  ( 1): tIn -- Fractional date.
%%% =----------------------------------------------------------------------
%%% = OUTPUTS
%%% =  ( 1): tOut -- Julian date.
%%% =======================================================================

function [ tOut ] = fracDateConv( tIn )
% Find a measurement from January 1st
[~,inds] = min(tIn - floor(tIn));
baseYear = datenum(floor(tIn(inds)),1,1);
tOut     = (tIn - tIn(inds)) * 365.25 + baseYear;
end


